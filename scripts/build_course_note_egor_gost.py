def set_gost_format(doc: Document) -> None:
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(14)
    pf = style.paragraph_format
    pf.line_spacing = 1.5
    pf.space_after = Pt(0)
    pf.space_before = Pt(0)
    pf.first_line_indent = Mm(12.5)

    for section in doc.sections:
        section.left_margin = Mm(30)
        section.right_margin = Mm(15)
        section.top_margin = Mm(20)
        section.bottom_margin = Mm(20)


def add_title_lines(doc: Document, lines: list[str]) -> None:
    for line in lines:
        p = doc.add_paragraph(line)
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.paragraph_format.first_line_indent = Mm(0)


def add_heading(doc: Document, text: str) -> None:
    p = doc.add_paragraph(text)
    p.style = "Heading 1"
    p.paragraph_format.first_line_indent = Mm(0)


def add_subheading(doc: Document, text: str) -> None:
    p = doc.add_paragraph(text)
    p.style = "Heading 2"
    p.paragraph_format.first_line_indent = Mm(0)


def add_block(doc: Document, text: str) -> None:
    parts = [part.strip() for part in text.split("\n\n")]
    for part in parts:
        if not part:
            p = doc.add_paragraph("")
            p.paragraph_format.first_line_indent = Mm(0)
            continue
        doc.add_paragraph(part)


def add_toc(doc: Document) -> None:
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Mm(0)
    r = p.add_run()
    fld_begin = OxmlElement("w:fldChar")
    fld_begin.set(qn("w:fldCharType"), "begin")
    instr = OxmlElement("w:instrText")
    instr.set(qn("xml:space"), "preserve")
    instr.text = 'TOC \\o "1-3" \\h \\z \\u'
    fld_separate = OxmlElement("w:fldChar")
    fld_separate.set(qn("w:fldCharType"), "separate")
    fld_end = OxmlElement("w:fldChar")
    fld_end.set(qn("w:fldCharType"), "end")
    r._r.append(fld_begin)
    r._r.append(instr)
    r._r.append(fld_separate)
    r._r.append(fld_end)


def apply_heading_styles(doc: Document) -> None:
    chapter_re = re.compile(r"^\d+\s+")
    sub_re = re.compile(r"^\d+\.\d+")
    for p in doc.paragraphs:
        text = p.text.strip()
        if not text:
            continue
        if sub_re.match(text):
            p.style = "Heading 2"
        elif chapter_re.match(text):
            p.style = "Heading 1"


def count_stats(doc: Document) -> tuple[int, int, int, int]:
    tables = len(doc.tables)
    figures = sum(
        1 for p in doc.paragraphs if p.text.strip().lower().startswith("рисунок")
    )
    listings = sum(
        1 for p in doc.paragraphs if p.text.strip().lower().startswith("листинг")
    )
    words = 0
    for p in doc.paragraphs:
        words += len(p.text.split())
    pages = max(35, int(math.ceil(words / 260)))
    return pages, figures, tables, listings


def replace_placeholders(doc: Document, mapping: dict[str, str]) -> None:
    for p in doc.paragraphs:
        for key, value in mapping.items():
            if key in p.text:
                p.text = p.text.replace(key, value)


def add_listing(doc: Document, title: str, lines: list[str]) -> None:
    if title:
        p = doc.add_paragraph(title)
        p.paragraph_format.first_line_indent = Mm(0)
    for line in lines:
        p = doc.add_paragraph()
        p.paragraph_format.first_line_indent = Mm(0)
        run = p.add_run(line)
        run.font.name = "Courier New"
        run.font.size = Pt(10)
    note = doc.add_paragraph("Примечание – Источник: собственная разработка")
    note.paragraph_format.first_line_indent = Mm(0)


def get_feature_importances(top_n: int = 15) -> list[tuple[str, str]]:
    model_path = Path("models/babd13_wallet_type_model.joblib")
    if not model_path.exists():
        return [("N/A", "0.000000")]

    payload = joblib.load(model_path)
    pipeline = payload.get("pipeline")
    if pipeline is None:
        return [("N/A", "0.000000")]

    vectorizer = pipeline.named_steps["vectorizer"]
    model = pipeline.named_steps["model"]
    names = vectorizer.get_feature_names_out()
    importances = model.feature_importances_
    order = np.argsort(importances)[::-1][:top_n]

    results: list[tuple[str, str]] = []
    for idx in order:
        results.append((names[idx], f"{importances[idx]:.6f}"))
    return results


